



# Метод count() считает количество непересекающихся вхождений подстроки <sub> в исходную строку s.
print(s.count('oo'))
print(s.count('oo', 0, 8))  # подсчет с 0 по 7 символ

##================================================================================

# Метод startswith() определяет, начинается ли исходная строка s подстрокой <prefix>. 
# Метод возвращает значение True, если исходная строка начинается с подстроки <prefix>, или False в противном случае.
s = 'foobar'
print(s.startswith('foo'))
print(s.startswith('baz'))

##================================================================================

# Метод endswith() определяет, оканчивается ли исходная строка s подстрокой <suffix>. 
# Метод возвращает значение True, если исходная строка оканчивается на подстроку <suffix>, или False в противном случае.
s = 'foobar'
print(s.endswith('bar'))
print(s.endswith('baz'))

