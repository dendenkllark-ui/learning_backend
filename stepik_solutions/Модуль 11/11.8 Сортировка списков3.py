# Сортировка простыми вставками

a = [1, 7, -3, 9, 0, -67, 34, 12, 45, 1000, 6,  8, -2, 99]
n = len(a)

for i in range(1, n): 
    elem = a[i]  # берем первый элемент из неотсортированной части списка
    j = i
    
    # пока элемент слева существует и больше нашего текущего элемента
    while j >= 1 and a[j - 1] > elem:
        # смещаем j-1-й элемент отсортированной части вправо
        a[j] = a[j - 1]
        # сами идём влево, дальше ищем место для нашего текущего элемента
        j -= 1

    # нашли место для нашего текущего элемента из неотсортированной части
    # и вставляем его на индекс j в отсортированной части
    a[j] = elem

print('Отсортированный список:', a)

# Оптимизация кода с помощью бинарного поиска

a = [1, 7, -3, 9, 0, -67, 34, 12, 45, 1000, 6, 8, -2, 99]
n = len(a)

for i in range(1, n):
    elem = a[i]
    
    # 1. БИНАРНЫЙ ПОИСК: ищем индекс, куда нужно вставить elem
    left = 0
    right = i - 1
    
    while left <= right:
        mid = (left + right) // 2
        if a[mid] > elem:
            right = mid - 1  # Ищем в левой половине
        else:
            left = mid + 1   # Ищем в правой половине
            
    # Переменная left теперь указывает на точное место для вставки
    insertion_point = left

    # 2. СДВИГ: смещаем все элементы от i-1 вниз до insertion_point вправо
    j = i
    while j > insertion_point:
        a[j] = a[j - 1]
        j -= 1
        
    # 3. ВСТАВКА: сажаем элемент на его законное место
    a[insertion_point] = elem

print('Отсортированный список:', a)